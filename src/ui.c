#include "ui.h"
void show_ui()
{
	//清屏
	printf("\033[2J \033[0m");

	printf("\033[9;3H\033[32m%s\033[0m","R");
	printf("\033[11;3H\033[32m%s\033[0m","T");
	//打印赛道
	int i = 0;
	for(i = 8;i<13;i++){
		//打印
		printf("\033[%d;4H\033[33m%s\033[0m",i,"|");
	}
	for(i = 5;i < 65;i++)
	{
		printf("\033[8;%dH\033[33m%s\033[0m",i,"-");
		printf("\033[10;%dH\033[33m%s\033[0m",i,"-");
		printf("\033[12;%dH\033[33m%s\033[0m",i,"-");

	}
	printf("\n");
	

}


